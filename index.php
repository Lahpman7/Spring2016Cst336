function dealHand($play,$seat)
{
   
  $numCards= rand(4,6);
  for($i=0;$i<$numCards;$i++)
  {
     array_push($play["Players"][$seat]["Hand"],$play["Deck"][$i] );
     unset($play["Deck"][$i]);
    $play["Players"][$seat]["Score"] +=$play["Players"][$seat]["Hand"][$i]["Value"];
  }
  //var_dump($play["Players"][$seat]["Hand"][0]["Value"]);
